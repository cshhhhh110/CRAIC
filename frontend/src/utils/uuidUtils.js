export const uuidCache = new UUIDCache();

/**
 * @description 业务实体UUID管理器
 * 用于管理不同业务实体的UUID生成和使用
 */
export class BusinessUUIDManager {
  constructor(businessType) {
    this.businessType = businessType;
    this.uuid = null;
    this.isGenerated = false;
  }

  /**
   * 生成UUID
   * @returns {string} 生成的UUID
   */
  generate() {
    if (!this.uuid) {
      this.uuid = generateBusinessUUID(this.businessType);
      this.isGenerated = true;
    }
    return this.uuid;
  }

  /**
   * 获取UUID（如果未生成则自动生成）
   * @returns {string} UUID
   */
  get() {
    return this.uuid || this.generate();
  }

  /**
   * 设置UUID
   * @param {string} uuid - UUID值
   */
  set(uuid) {
    if (isValidUUID(uuid)) {
      this.uuid = uuid;
      this.isGenerated = false; // 标记为外部设置
    } else {
      throw new Error('Invalid UUID format');
    }
  }

  /**
   * 重置UUID
   */
  reset() {
    this.uuid = null;
    this.isGenerated = false;
  }

  /**
   * 是否已生成UUID
   * @returns {boolean}
   */
  hasUUID() {
    return !!this.uuid;
  }

  /**
   * 是否为自动生成的UUID
   * @returns {boolean}
   */
  isAutoGenerated() {
    return this.isGenerated;
  }
}

/**
 * @description 创建业务UUID管理器的工厂函数
 * @param {string} businessType - 业务类型
 * @returns {BusinessUUIDManager} UUID管理器实例
 */
export function createBusinessUUIDManager(businessType) {
  return new BusinessUUIDManager(businessType);
}

