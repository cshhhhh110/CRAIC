/**
 * @description 检查业务类型是否支持UUID策略B
 * @param {string} businessType - 业务类型
 * @returns {boolean} 是否支持UUID策略B
 */
export function supportsUUIDStrategyB(businessType) {
  const supportedTypes = [
    BUSINESS_TYPES.HERITAGE_ITEM,
    BUSINESS_TYPES.ACTIVITY,
    BUSINESS_TYPES.COURSE,
    BUSINESS_TYPES.SHOP_PRODUCT,
    BUSINESS_TYPES.INHERITOR
  ];
  return supportedTypes.includes(businessType);
}







